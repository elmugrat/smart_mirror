var NUM_NEXT_DEPARTURES = 3;

function refreshDepartures() {
	removeFirstExpiredLi('bindestr_rathaus', 'M3', getNextDepartures(fahrplan.bindestr_rathaus.M3));
	
	removeFirstExpiredLi('gliesmaroderstr_uni', 'M19', getNextDepartures(fahrplan.gliesmaroderstr_uni.M19));
	
	removeFirstExpiredLi('gliesmaroderstr_hbf', 'M29', getNextDepartures(fahrplan.gliesmaroderstr_hbf.M29));
}

function removeFirstExpiredLi(targetDivId, transitLineName, nextDepartures) {	
	var li = $('div#' + targetDivId + ' ul li:first-child');

	if (li.length > 0 && !li.is('#' + nextDepartures[0].replace(':', ''))) {
		li.animate({opacity: 0, width: 0}, function() { 
			$(this).remove();
			removeFirstExpiredLi(targetDivId, transitLineName, nextDepartures);
		});
	} else {
		addNextLis(targetDivId, nextDepartures, transitLineName);
	}
}

function addNextLis(targetDivId, nextDepartures, transitLineName) {
	var nRemainingLis = $('div#' + targetDivId + ' ul li').length;
	
	for (n = 0; n < NUM_NEXT_DEPARTURES; n++) {
		if (n >= nRemainingLis) {
			$('div#' + targetDivId + ' ul').append('<li id="' + nextDepartures[n].replace(':', '') + '">' + nextDepartures[n] + '</li>');
		}
	}
}

function getDayString(day) {
	switch (day) {
	case 0:
		return "SUN";
	case 6:
		return "SAT";
	default:
		return "MTWRF";
	}
}

function getNextDepartures(weeklyPlan) {
	var day = parseInt(moment().format('d'));
	var now = moment();
	
	var nextDepartures = [];
	
	var departures = weeklyPlan[getDayString(day)];
	for (i in departures) {
		// if in the future, push to next --> if next.length >= the desired number of next departures, we're done
		if (moment(departures[i], "HH:mm").diff(now, 'minutes') >= 0 && nextDepartures.push(departures[i]) >= NUM_NEXT_DEPARTURES) {
			break;	
		}
	}
	
	// if necessary, fill next departures with the first entries from the next day
	if (nextDepartures.length < NUM_NEXT_DEPARTURES) {
		var departuresTmrw = weeklyPlan[getDayString((day + 1) % 7)];
		var n = 0;
		while (nextDepartures.push(departuresTmrw[n++]) < NUM_NEXT_DEPARTURES);
	}
	
	return nextDepartures;
}